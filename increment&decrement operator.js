//Increment and Decrement Operator
// ++ = increment
// increment number returns the value before incrementing.
//postfix increment
console.log("postfix")
var number = 0;
console.log(number++);
console.log(number);
console.log(number--);
console.log(number);

//prefix increment- returns the value after incrementing it.
console.log("prefix")
var numberTwo = 0;
console.log(++numberTwo);
console.log(--numberTwo);

console.log("for loop")
for (var p = 0; p <= 10; p++) {
    console.log(p);
}